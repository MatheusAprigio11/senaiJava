package math;

public abstract class operations {
    public abstract double areaTriang(double n1, double n2);
    public abstract double areaCirc(double pi, double r);
    public abstract double imc(double p, double h);
    public abstract double vm(double desl, double tempo);
    public abstract double forc(double massa, double acel);
    public abstract double distan(double vel, double tempo);
}
